use std::io::{self, Write};

fn main() {
    let mut stdout = io::stdout().lock();

    writeln!(stdout, "\\    /\\").unwrap();
    writeln!(stdout, " )  ( ')").unwrap();
    writeln!(stdout, "(  /  )").unwrap();
    writeln!(stdout, " \\(__)|").unwrap();
}
